function isEven(number) {
      if (number % 2 === 0) {
            return true;
      } else {
            return false;
      }     
}
// console.log(isEven(5));
// console.log(isEven(10));

function ifOdd(number) {
      if (number % 2 === 1) {
            return true;
      } else {
            return false;
      }     
}

console.log(ifOdd(5));
console.log(ifOdd(10));